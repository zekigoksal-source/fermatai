# ─── Konfigürasyon ──────────────────────────────────────────────────────────

REPO_PATH = "/opt/fermatai" if os.path.isdir("/opt/fermatai/.git") else "."

# HARDCODED YASAK — değiştirilemez
PROTECTED_BRANCHES = {"main", "master", "production", "develop", "release"}
BRANCH_PATTERN = re.compile(r"^bot/draft-\d{8}-\d+$")
DANGEROUS_GIT_FLAGS = {"--force", "-f", "--force-with-lease", "--force-if-includes",
                       "--mirror", "--all"}

# Daily quota
MAX_BRANCHES_PER_DAY = 10        # bot 1 günde max 10 branch açabilir
MAX_COMMITS_PER_BRANCH = 20      # bir branch'te max 20 commit


def _bot_signature() -> tuple[str, str]:
    """Commit author bilgileri — bot ile insan ayrımı."""
    return ("FermatAI Bot", "bot@fermatai.local")


def _commit_footer() -> str:
    """Her commit message'a eklenen Co-Author satırı."""
    return (
        "\n\n"
        "🤖 Generated by FermatAI Self-Dev (Evre 2.2)\n"
        "Co-Authored-By: FermatAI Bot <bot@fermatai.local>"
    )


async def _push_enabled() -> bool:
    """SELF_DEV_PUSH_ENABLED flag — default false."""
    try:
        from db_pool import db_fetchval
        v = await db_fetchval(
            "SELECT value FROM sistem_ayar WHERE key = 'SELF_DEV_PUSH_ENABLED'"
        )
        return (str(v or "false").lower() == "true")
    except Exception:
        return False


def _run_git(args: list[str], timeout: int = 30) -> tuple[int, str, str]:
    """git komutu çalıştır. (returncode, stdout, stderr)"""
    # Tehlikeli flag'leri filtrele
    for arg in args:
        if arg in DANGEROUS_GIT_FLAGS:
            return 99, "", f"YASAK FLAG: {arg}"
    cmd = ["git", "-C", REPO_PATH] + args
    try:
        proc = subprocess.run(cmd, capture_output=True, text=True,
                              timeout=timeout, errors="replace")
        return proc.returncode, proc.stdout.strip(), proc.stderr.strip()
    except subprocess.TimeoutExpired:
        return 98, "", "timeout"
    except Exception as e:
        return 97, "", str(e)


async def _check_daily_branch_quota(actor: str) -> tuple[bool, int]:
    """Bot bugün kaç branch açtı?"""
    from db_pool import db_fetchval
    try:
        n = await db_fetchval(
            """SELECT COUNT(*) FROM self_dev_audit
               WHERE actor_phone = $1
                 AND tool_name = 'create_draft_branch'
                 AND success = true
                 AND created_at > NOW() - INTERVAL '24 hours'""",
            actor,
        ) or 0
        return (int(n) < MAX_BRANCHES_PER_DAY), int(n)
    except Exception:
        return True, 0


async def _next_draft_branch_name(date_str: Optional[str] = None) -> str:
    """Bugün için sıradaki bot/draft-YYYYMMDD-N adı."""
    if not date_str:
        date_str = datetime.utcnow().strftime("%Y%m%d")
    rc, out, err = _run_git(["branch", "--list", f"bot/draft-{date_str}-*"])
    if rc == 0:
        existing = [b.strip().lstrip("* ") for b in out.splitlines() if b.strip()]
        nums = []
        for b in existing:
            m = re.match(rf"^bot/draft-{date_str}-(\d+)$", b)
            if m:
                nums.append(int(m.group(1)))
        next_num = (max(nums) + 1) if nums else 1
    else:
        next_num = 1
    return f"bot/draft-{date_str}-{next_num}"


