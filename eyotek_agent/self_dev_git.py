async def set_push_enabled(enabled: bool, by_phone: str = "") -> dict:
    """SELF_DEV_PUSH_ENABLED flag toggle."""
    from db_pool import get_pool
    try:
        pool = await get_pool()
        async with pool.acquire() as conn:
            await conn.execute(
                """INSERT INTO sistem_ayar (key, value, updated_at)
                   VALUES ('SELF_DEV_PUSH_ENABLED', $1, NOW())
                   ON CONFLICT (key) DO UPDATE SET value = $1, updated_at = NOW()""",
                "true" if enabled else "false",
            )
        from self_dev_tools import _audit
        await _audit(by_phone, "set_push_enabled", {"enabled": enabled}, True)
        return {
            "ok": True,
            "enabled": enabled,
            "message": (
                "Self-dev push AÇIK ✅ (Bot artık bot/draft-* branch'leri GitHub'a push edebilir)"
                if enabled else
                "Self-dev push KAPALI ⛔ (Bot lokal'de kalır)"
            ),
        }
    except Exception as e:
        return {"ok": False, "error": str(e)[:200]}


